            <h1>About DwarfShack</h1>
            <p><span style="font-family: 'Glass Antiqua', cursive; font-size: 48px;">D</span>warfShack is a <a href="http://www.bay12games.com/dwarves/" target="_blank">Dwarf Fortress</a> succession game that is hosted by <a href="http://www.shacknews.com/chatty" target="_blank">Shacknews</a> members. Here we host stories, <a href="tutorials.php">tutorials</a>, and <a href="resources.php">resources</a> for players new to DF.</p>
			<p>A succession game is where one player plays an in-game year of Dwarf Fortress, saves his/her game, and passes it on to another player who then does the same. During the year that you rule, you  document it for others to read, sort of like a journal/diary.</p>
			<p>The documentation is usually written in first-person and is accompanied by screenshots to tell the story of your one-year reign. These stories usually lead to some hilarious moments. Players often name a dwarf after them when their year has come to pass, leaving hilarity to be had in a future ruler's year.</p>
			<p>You can read about previous Shacknews Succession games <a href="http://shackwiki.com/wiki/Dwarf_Fortress_Succession_2010" target="_blank">here</a></p>
			<p>One of the most popular succession games to date is the famous "<a href="http://lparchive.org/Dwarf-Fortress-Boatmurdered/" target="_blank">Boatmurdered</a>".</p>
